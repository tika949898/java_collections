public class Array2D1 {

  public static void main(String[] args) {
    int[][] myNumber = {
    {1, 4, 2},
    {3, 8, 6, 5, 9}
    };
    System.out.println("Rows: " + myNumber.length);
    System.out.println("Cols in row 0: " + myNumber[0].length);
    System.out.println("Cols in row 1: " + myNumber[1].length);
  }
}