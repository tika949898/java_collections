public class Comparison {
public static void main(String[] args) {
        int x = 12;
        int y = 12;
        System.out.println(x > y);
        System.out.println(x == y);
        System.out.println(x != y);
        System.out.println(x < y);
        System.out.println(x <= y);
        System.out.print(x >= y);
    }
}